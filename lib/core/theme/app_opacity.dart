/// AgriLink Edge — Opacity System
///
/// Standardized opacity values. Never use arbitrary opacity values inline.
abstract final class AppOpacity {
  static const double invisible = 0.0;
  static const double ghost = 0.08;
  static const double faint = 0.12;
  static const double low = 0.16;
  static const double subtle = 0.24;
  static const double disabled = 0.38;
  static const double medium = 0.50;
  static const double moderate = 0.60;
  static const double high = 0.80;
  static const double full = 1.0;
}
